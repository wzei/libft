#include "libft.h"

int	ft_strncmp(char *s1, char *s2, size_t n)
{
	int iter;

	if (n == 0)
		return (0);
	iter = 0;
	while (s1[iter] != '\0' && s2[iter] != '\0' && n != 0)
	{
		if (s1[iter] != s2[iter])
			break ;
		iter++;
		n--;
	}
	return (s1[iter] - s2[iter]);
}
