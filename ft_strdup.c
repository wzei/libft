#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	void	*new;

	len = ft_strlen(s1) + 1;
	new = malloc(len * sizeof(char));
	if (new == NULL)
		return (NULL);
	return ((char *)ft_memcpy(new, s1, len));
}
